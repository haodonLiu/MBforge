import '@testing-library/jest-dom'

// jsdom does not implement matchMedia — provide a stub so components
// using useIsMobile (or any matchMedia call) do not crash in tests.
Object.defineProperty(window, 'matchMedia', {
  writable: true,
  value: (query: string) => ({
    matches: false,
    media: query,
    onchange: null,
    addListener: () => {},
    removeListener: () => {},
    addEventListener: () => {},
    removeEventListener: () => {},
    dispatchEvent: () => false,
  }),
})
